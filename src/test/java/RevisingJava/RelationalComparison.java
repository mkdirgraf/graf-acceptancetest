package RevisingJava;

public class RelationalComparison {
    public static void main(String[] args) {
        int n1=50;
        int n2=55;
        System.out.println(n1>n2);

        boolean test=n1<=n2;
        System.out.println(test);

        boolean test2=n1!=n2;
        System.out.println(test2);
    }
}
